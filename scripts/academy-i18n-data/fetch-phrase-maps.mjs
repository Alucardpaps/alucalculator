/**
 * Fetches phrase maps via MyMemory API (cached). Run once:
 * node scripts/academy-i18n-data/fetch-phrase-maps.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..', '..');
const ARTICLES = path.join(ROOT, 'src/data/academy-articles');
const CACHE = path.join(__dirname, 'translation-tables/phrases/cache.json');
const OUT_DIR = path.join(__dirname, 'translation-tables/phrases');

const LANGS = [
  { code: 'de', pair: 'en|de' },
  { code: 'es', pair: 'en|es' },
  { code: 'fr', pair: 'en|fr' },
  { code: 'it', pair: 'en|it' },
  { code: 'pt', pair: 'en|pt' },
  { code: 'ru', pair: 'en|ru' },
  { code: 'zh', pair: 'en|zh-CN' },
  { code: 'ja', pair: 'en|ja' },
  { code: 'ko', pair: 'en|ko' },
  { code: 'ar', pair: 'en|ar' },
];

function collectAllStrings() {
  const set = new Set();
  const enrich = JSON.parse(
    fs.readFileSync(path.join(__dirname, 'seed-data-sources/en-enrichment.json'), 'utf8'),
  );
  for (const v of Object.values(enrich)) {
    v.learningObjectives.forEach((s) => set.add(s));
    v.keyTakeaways.forEach((s) => set.add(s));
    if (v.supplementalParagraph) set.add(v.supplementalParagraph);
  }
  for (const f of fs.readdirSync(ARTICLES).filter((x) => x.endsWith('.json'))) {
    const a = JSON.parse(fs.readFileSync(path.join(ARTICLES, f), 'utf8'));
    (a.stepByStep ?? []).forEach((s) => set.add(s));
    (a.whyThisMatters ?? []).forEach((s) => set.add(s));
    (a.commonMistakes ?? []).forEach((s) => set.add(s));
    if (a.engineeringTip) set.add(a.engineeringTip);
    (a.faq ?? []).forEach((q) => {
      set.add(q.question);
      set.add(q.answer);
    });
    if (a.example) {
      (a.example.inputs ?? []).forEach((s) => set.add(s));
      if (a.example.calculation) set.add(a.example.calculation);
    }
    if (a.formula?.variables) {
      Object.values(a.formula.variables).forEach((s) => set.add(s));
    }
    if (a.calculatorCta?.label) set.add(a.calculatorCta.label);
  }
  return [...set];
}

function loadCache() {
  if (fs.existsSync(CACHE)) return JSON.parse(fs.readFileSync(CACHE, 'utf8'));
  return {};
}

function saveCache(cache) {
  fs.mkdirSync(path.dirname(CACHE), { recursive: true });
  fs.writeFileSync(CACHE, JSON.stringify(cache, null, 2), 'utf8');
}

async function translate(text, pair, attempt = 0) {
  const [sl, tl] = pair.split('|');
  const tlCode = tl === 'zh-CN' ? 'zh-CN' : tl;
  const url = `https://translate.googleapis.com/translate_a/single?client=gtx&sl=${sl}&tl=${tlCode}&dt=t&q=${encodeURIComponent(text)}`;
  try {
    const res = await fetch(url);
    if (!res.ok) throw new Error(`Translate HTTP ${res.status} (${pair})`);
    const json = await res.json();
    const out = json[0]?.map((x) => x[0]).join('') ?? '';
    if (!out) throw new Error(`Empty translation (${pair}): ${text.slice(0, 40)}`);
    return out;
  } catch (e) {
    if (attempt < 5) {
      await sleep(800 * (attempt + 1));
      return translate(text, pair, attempt + 1);
    }
    throw e;
  }
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function main() {
  const keys = collectAllStrings();
  console.log(`Collecting ${keys.length} unique strings`);
  fs.mkdirSync(OUT_DIR, { recursive: true });

  const cache = loadCache();

  for (const { code, pair } of LANGS) {
    cache[code] = cache[code] ?? {};
    const phrases = {};
    let done = 0;
    for (const en of keys) {
      if (cache[code][en]) {
        phrases[en] = cache[code][en];
        done++;
        continue;
      }
      await sleep(200);
      const tr = await translate(en, pair);
      phrases[en] = tr;
      cache[code][en] = tr;
      done++;
      if (done % 20 === 0) {
        saveCache(cache);
        console.log(`${code}: ${done}/${keys.length}`);
      }
    }
    saveCache(cache);
    const content = `/** Auto-generated by fetch-phrase-maps.mjs — ${code} */
export const PHRASES = ${JSON.stringify(phrases, null, 2)};
`;
    fs.writeFileSync(path.join(OUT_DIR, `${code}.mjs`), content, 'utf8');
    console.log(`Wrote phrases/${code}.mjs (${Object.keys(phrases).length} entries)`);
  }
  console.log('Done.');
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
