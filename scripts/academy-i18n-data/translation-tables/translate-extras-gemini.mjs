/**
 * Translates academy extras (walkthroughs, quizzes, practice) from DE template to all langs.
 * Run: node scripts/academy-i18n-data/translation-tables/translate-extras-gemini.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { GoogleGenerativeAI } from '@google/generative-ai';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..', '..', '..');

const LANGS = [
  { code: 'es', name: 'Spanish' },
  { code: 'fr', name: 'French' },
  { code: 'it', name: 'Italian' },
  { code: 'pt', name: 'Portuguese' },
  { code: 'ru', name: 'Russian' },
  { code: 'zh', name: 'Simplified Chinese' },
  { code: 'ja', name: 'Japanese' },
  { code: 'ko', name: 'Korean' },
  { code: 'ar', name: 'Arabic' },
];

function loadApiKey() {
  const envPath = path.join(ROOT, '.env.local');
  const raw = fs.readFileSync(envPath, 'utf8');
  const m = raw.match(/GEMINI_API_KEY=["']?([^"'\n]+)/);
  if (!m) throw new Error('GEMINI_API_KEY not found');
  return m[1];
}

// DE extras from generate-translation-data.mjs (inline import via eval of EXTRAS.de)
const genDataPath = path.join(__dirname, '..', 'locale-data-src', 'generate-translation-data.mjs');
const genSrc = fs.readFileSync(genDataPath, 'utf8');
const deMatch = genSrc.match(/const EXTRAS = \{[\s\S]*?de: (\{[\s\S]*?\n  \}),\n\};/);
if (!deMatch) throw new Error('Could not extract DE extras from generate-translation-data.mjs');
const deExtras = eval(`(${deMatch[1]})`);

const genAI = new GoogleGenerativeAI(loadApiKey());
const model = genAI.getGenerativeModel({ model: 'gemini-2.0-flash' });

const cacheDir = path.join(__dirname, 'extras-cache');
fs.mkdirSync(cacheDir, { recursive: true });

async function translateExtras(lang) {
  const cacheFile = path.join(cacheDir, `${lang.code}.json`);
  if (fs.existsSync(cacheFile)) {
    console.log(`Using cache for ${lang.code}`);
    return JSON.parse(fs.readFileSync(cacheFile, 'utf8'));
  }

  const prompt = `Translate this JSON object from German to ${lang.name} for a mechanical engineering academy.

Rules:
- Translate ALL human-readable strings (titles, details, questions, options, explanations, labels).
- Keep JSON keys, slugs, ids, sourceFile paths, formulas, variable names (σx, τxy, L10, etc.) unchanged.
- Keep technical symbols and units.
- Return ONLY valid JSON with identical structure.

German JSON:
${JSON.stringify(deExtras, null, 2)}`;

  const result = await model.generateContent(prompt);
  const text = result.response.text().trim();
  const jsonMatch = text.match(/\{[\s\S]*\}/);
  if (!jsonMatch) throw new Error(`No JSON for ${lang.code}`);
  const translated = JSON.parse(jsonMatch[0]);
  fs.writeFileSync(cacheFile, JSON.stringify(translated, null, 2), 'utf8');
  console.log(`Cached extras for ${lang.code}`);
  await new Promise((r) => setTimeout(r, 2000));
  return translated;
}

async function main() {
  const allExtras = { de: deExtras };
  for (const lang of LANGS) {
    console.log(`Translating extras → ${lang.code}...`);
    allExtras[lang.code] = await translateExtras(lang);
  }

  const outPath = path.join(__dirname, '..', 'locale-data-src', 'lang-extras.mjs');
  const mapPath = path.join(__dirname, 'extras-map.mjs');
  const content = `/** Auto-generated by translate-extras-gemini.mjs */\nexport const LANG_EXTRAS = ${JSON.stringify(allExtras, null, 2)};\n`;
  const mapContent = `/** Auto-generated extras for write-all-locales.mjs */\nexport const EXTRAS_MAP = ${JSON.stringify(allExtras, null, 2)};\n`;
  fs.writeFileSync(outPath, content, 'utf8');
  fs.writeFileSync(mapPath, mapContent, 'utf8');
  console.log('Wrote lang-extras.mjs and extras-map.mjs');
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
